// Deduplicate by slug — keep the one with all merged free periods
const slugMap = new Map<string, FreeGame>();
for (const game of ALL_GAMES) {
  const existing = slugMap.get(game.slug);
  if (existing) {
    // Merge periods and deduplicate
    const allPeriods = [...existing.freePeriods, ...game.freePeriods];
    const unique = allPeriods.filter(
      (p, i, arr) => arr.findIndex((q) => q.start === p.start) === i
    );
    unique.sort((a, b) => a.start.localeCompare(b.start));
    existing.freePeriods = unique;
  } else {
    slugMap.set(game.slug, { ...game });
  }
}

export const GAMES: FreeGame[] = Array.from(slugMap.values()).sort((a, b) => {
  const aLatest = a.freePeriods[a.freePeriods.length - 1].start;
  const bLatest = b.freePeriods[b.freePeriods.length - 1].start;
  return bLatest.localeCompare(aLatest);
});

export const ALL_GENRES = Array.from(new Set(GAMES.map((g) => g.genre))).sort();
export const ALL_YEARS = Array.from(new Set(GAMES.flatMap((g) => g.freePeriods.map((p) => new Date(p.start).getFullYear())))).sort((a, b) => b - a);
