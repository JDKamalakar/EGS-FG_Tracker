export interface FreeGame {
  slug: string;
  name: string;
  freePeriods: { start: string; end: string }[];
  genre: string;
  year: number;
  coverUrl?: string;
}

function slugify(name: string): string {
  return name.toLowerCase().replace(/[^a-z0-9]+/g, '-').replace(/^-|-$/g, '');
}

function makeGame(
  name: string,
  freePeriods: { start: string; end: string }[],
  genre: string
): FreeGame {
  const years = freePeriods.map((p) => new Date(p.start).getFullYear());
  const year = Math.min(...years);
  return { slug: slugify(name), name, freePeriods, genre, year };
}

